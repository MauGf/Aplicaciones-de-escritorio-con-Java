    private void btnGenerarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnGenerarActionPerformed
        String primos = "";
        int numero;
        int cont = 0;
        int cpi = 0;
        primos= "Los numeros primos comprendidos entre 1 y "+ txtNum.getText() +" son : "+ "\n";
        try {
            numero = Integer.parseInt(txtNum.getText());

        } catch (Exception ex) {
            JOptionPane.showMessageDialog(null, "Dato invÃ¡lido", "ERROR", JOptionPane.ERROR_MESSAGE);
            return;
        }
        for (int i = numero; i > 0; --i) {
            for (int j = 1; j <= numero; j++) {
                if (i % j == 0) {
                    cont = cont + 1;
                }
            }
            if (cont < 3) {
                primos += "=." + i +"\n";
                cpi++;
            }
            cont = 0;
        }
        JOptionPane.showMessageDialog(null, primos);
    }//GEN-LAST:event_btnGenerarActionPerformed

