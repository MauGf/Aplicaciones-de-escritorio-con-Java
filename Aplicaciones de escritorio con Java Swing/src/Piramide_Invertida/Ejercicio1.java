    private void btnGenerarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnGenerarActionPerformed
        String Salida;
        int altura = 0;
        Salida = "La piramide invertida es de  "+ texto.getText() + " Lineas" +"\n\n";
        try {
            altura = Integer.parseInt(texto.getText());
        } catch (Exception ex) {
            JOptionPane.showMessageDialog(null, "Dato invÃ¡lido", "ERROR", JOptionPane.ERROR_MESSAGE);
            return;
        } 
        for (int numeroespacios=0, numeroasteriscos=(altura*2)-1; numeroasteriscos>0;numeroespacios++, numeroasteriscos-=2){
 
            for (int i=0; i<numeroespacios;i++){
                Salida +=(" ") ;
            }
 
            for (int j=numeroasteriscos; j!=0;j--){
               Salida +=("*");
            }
            Salida +=("")+"\n" ;
        }
        JOptionPane.showMessageDialog(null, Salida, "Piramide Invertida", JOptionPane.INFORMATION_MESSAGE);
    
    }//GEN-LAST:event_btnGenerarActionPerformed
         
